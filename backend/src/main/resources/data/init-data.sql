USE heritage;
SET FOREIGN_KEY_CHECKS = 0;
SET NAMES utf8mb4;

-- ============================================
-- 第一部分：创建表结构
-- ============================================

CREATE TABLE IF NOT EXISTS heritage_categories (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    parent_id BIGINT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS heritage_projects (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(200) NOT NULL,
    category_id BIGINT,
    intro TEXT,
    history TEXT,
    craftsmanship TEXT,
    inheritance TEXT,
    status_text TEXT,
    cover_image VARCHAR(500),
    location VARCHAR(200),
    region VARCHAR(100),
    longitude DOUBLE,
    latitude DOUBLE,
    tags VARCHAR(500),
    publish_status VARCHAR(50) DEFAULT 'DRAFT',
    created_by BIGINT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (category_id) REFERENCES heritage_categories(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS inheritors (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    bio VARCHAR(2000),
    skills VARCHAR(1000),
    avatar_url VARCHAR(500),
    contact VARCHAR(200),
    region VARCHAR(100),
    level VARCHAR(50)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS activities (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    title VARCHAR(200) NOT NULL,
    project_id BIGINT,
    location VARCHAR(200),
    start_time DATETIME,
    end_time DATETIME,
    capacity INT,
    status VARCHAR(50) DEFAULT 'PUBLISHED',
    description TEXT,
    cover VARCHAR(500)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS product_categories (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    parent_id BIGINT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS products (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    category_id BIGINT NOT NULL,
    name VARCHAR(200) NOT NULL,
    description VARCHAR(2000),
    price DECIMAL(10, 2) NOT NULL,
    stock INT NOT NULL DEFAULT 0,
    cover VARCHAR(500),
    images VARCHAR(2000),
    status VARCHAR(50) DEFAULT 'ONSALE',
    sales_count BIGINT DEFAULT 0,
    FOREIGN KEY (category_id) REFERENCES product_categories(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS page_banners (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    page_key VARCHAR(50) UNIQUE NOT NULL,
    title VARCHAR(200),
    subtitle VARCHAR(500),
    image_url VARCHAR(500),
    status VARCHAR(50) DEFAULT 'enabled'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS project_inheritors (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    project_id BIGINT NOT NULL,
    inheritor_id BIGINT NOT NULL,
    UNIQUE KEY unique_project_inheritor (project_id, inheritor_id),
    FOREIGN KEY (project_id) REFERENCES heritage_projects(id),
    FOREIGN KEY (inheritor_id) REFERENCES inheritors(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS media_assets (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    type VARCHAR(50) NOT NULL,
    url VARCHAR(500) NOT NULL,
    title VARCHAR(200),
    project_id BIGINT,
    inheritor_id BIGINT,
    duration INT,
    size BIGINT,
    transcript VARCHAR(4000),
    hls_url VARCHAR(500),
    cover_url VARCHAR(500),
    status VARCHAR(50),
    category VARCHAR(100),
    tags VARCHAR(500),
    view_count INT DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (project_id) REFERENCES heritage_projects(id),
    FOREIGN KEY (inheritor_id) REFERENCES inheritors(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS news (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    title VARCHAR(200) NOT NULL,
    content VARCHAR(4000),
    type VARCHAR(50) NOT NULL,
    cover_image VARCHAR(500),
    publish_time TIMESTAMP,
    status VARCHAR(50) DEFAULT 'DRAFT'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS videos (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    title VARCHAR(200) NOT NULL,
    url VARCHAR(500) NOT NULL,
    cover VARCHAR(500),
    duration INT,
    status VARCHAR(50) DEFAULT 'PUBLISHED'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS banners (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    image_url VARCHAR(500) NOT NULL,
    link VARCHAR(500),
    sort_order INT DEFAULT 0,
    status VARCHAR(50) DEFAULT 'enabled'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

